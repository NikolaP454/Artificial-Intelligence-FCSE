import os
os.environ['OPENBLAS_NUM_THREADS'] = '1'

# Imports
import pandas as pd

from typing import List

from sklearn.model_selection import train_test_split
from sklearn.preprocessing import OrdinalEncoder
from sklearn.tree import DecisionTreeClassifier
from sklearn.pipeline import make_pipeline
from sklearn.metrics import accuracy_score


def main(data: List[List[str]]) -> None:
    """
    Main function for training and evaluating a decision tree classifier.

    Args:
        data (List[List[str]]): The input data used for training and evaluation.

    Returns:
        None
    """
    
    # Data Split
    X = pd.DataFrame([row[:-1] for row in data])
    y = pd.Series([row[-1] for row in data])

    # Train-Test Split (75-25%)
    # We put X_test and y_test first, because we want to use the last X% of the dataset for training
    train_percent = int(input())
    X_test, X_train, y_test, y_train = train_test_split(X, y, shuffle=False, test_size=train_percent/100)
    
    # Model Generation
    model = DecisionTreeClassifier(
        criterion=input(),
        random_state=0
    )
    
    # Pipeline generation
    pipeline = make_pipeline(
        OrdinalEncoder(),   # Preprocessing
        model               # Model
    )
    
    pipeline.fit(X_train, y_train)
    
    # Retrain Encoder due to missing category in Training set
    pipeline[0].fit(X)
    
    # Model Evaluation
    print(f'Depth: {pipeline[1].get_depth()}')
    print(f'Number of leaves: {pipeline[1].get_n_leaves()}')
    print(f'Accuracy: {accuracy_score(y_test, pipeline.predict(X_test))}')

    importance = list(pipeline[1].feature_importances_)
    print(f'Most important feature: {importance.index(max(importance))}')
    print(f'Least important feature: {importance.index(min(importance))}')


